package com.projectx.data.domain;

public class VerifyLoginDetailsDataDTO {

	private String email;
	
	private Long mobile;
	
	private String password;

	public VerifyLoginDetailsDataDTO() {
		super();
	}

	public VerifyLoginDetailsDataDTO(String email, Long mobile, String password) {
		super();
		this.email = email;
		this.mobile = mobile;
		this.password = password;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public Long getMobile() {
		return mobile;
	}

	public void setMobile(Long mobile) {
		this.mobile = mobile;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	
	
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((email == null) ? 0 : email.hashCode());
		result = prime * result + ((mobile == null) ? 0 : mobile.hashCode());
		result = prime * result
				+ ((password == null) ? 0 : password.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		VerifyLoginDetailsDataDTO other = (VerifyLoginDetailsDataDTO) obj;
		if (email == null) {
			if (other.email != null)
				return false;
		} else if (!email.equals(other.email))
			return false;
		if (mobile == null) {
			if (other.mobile != null)
				return false;
		} else if (!mobile.equals(other.mobile))
			return false;
		if (password == null) {
			if (other.password != null)
				return false;
		} else if (!password.equals(other.password))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "VerifyLoginDetailsDataDTO [email=" + email + ", mobile="
				+ mobile + ", password=" + password + "]";
	}
	
	
	
	
	
	
	
}
